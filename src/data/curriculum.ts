export function getLessonIndex(id: string): number {
  return guidedPath.indexOf(id);
}

export function getModuleForLesson(lessonId: string): Module | undefined {
  const lesson = lessons[lessonId];
  if (!lesson) return undefined;
  return modules.find(m => m.id === lesson.module);
}

export function getNextLesson(id: string): Lesson | undefined {
  const idx = guidedPath.indexOf(id);
  if (idx < 0 || idx >= guidedPath.length - 1) return undefined;
  return lessons[guidedPath[idx + 1]];
}

export function getPrevLesson(id: string): Lesson | undefined {
  const idx = guidedPath.indexOf(id);
  if (idx <= 0) return undefined;
  return lessons[guidedPath[idx - 1]];
}
